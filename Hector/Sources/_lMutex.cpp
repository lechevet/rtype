#include "_lMutex.h"

/*
=============================================================================
	CONSTRUCTEUR DE LA CLASS _lMutex
	: INITIALISATION DU MUTEX
=============================================================================
*/
_lMutex::_lMutex()
{
	this->_mutex = PTHREAD_MUTEX_INITIALIZER;	
}

/*
=============================================================================
	DESTRUCTEUR DE LA CLASS _lMutex
	: Unlock le mutex
=============================================================================
*/
_lMutex::~_lMutex()
{
	this->unlockMutex();
}
/*
=============================================================================
	Lock le mutex
=============================================================================
*/
int _lMutex::lockMutex()
{
	return (pthread_mutex_lock(&(this->_mutex)));
}
/*
=============================================================================
	Unlock le Mutex
=============================================================================
*/
int _lMutex::unlockMutex()
{
	return (pthread_mutex_unlock(&(this->_mutex)));
}